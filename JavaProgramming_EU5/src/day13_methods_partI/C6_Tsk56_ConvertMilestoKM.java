package day13_methods_partI;

public class C6_Tsk56_ConvertMilestoKM {

	public static void main(String[] args) {
	
		
		convertKM();
		convertKM();
		

	}

	public static void convertKM() {
		
		int mile=80;
		double km=mile * 1.60934;
		
		System.out.println(mile + " mile = "+ km + " km");
		
		
	}

}
