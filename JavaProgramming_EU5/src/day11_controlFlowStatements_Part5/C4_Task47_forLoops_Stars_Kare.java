package day11_controlFlowStatements_Part5;

public class C4_Task47_forLoops_Stars_Kare {

	public static void main(String[] args) {
		
		int rows=7;
		int columns=6;
		
		for (int i = 1; i <=rows; i++) {
					
			for (int j = 1; j <=columns; j++) {
				System.out.print("* ");
			}
			
			System.out.println();
		}
		

	}

}
