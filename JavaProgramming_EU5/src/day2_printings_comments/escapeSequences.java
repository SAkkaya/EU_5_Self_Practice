package day2_printings_comments;

public class escapeSequences {

	public static void main(String[] args) {
		
		System.out.println("Learn\tJava\n\tthe\nEasy\tWay\n\n"); //Learn	Java
																 //the
																 //Easy	Way

		System.out.println("\tLearn Java the \"Easy\" Way!\n");  //Learn Java the "Easy" Way!
		
		System.out.println("\\ // -=- \\ //");  // -->   \ // -=- \ //
		
		System.out.println("\\\\ \\\\\\ \\\\\\\\");   //--->     \\ \\\ \\\\
		
		
	}

}
