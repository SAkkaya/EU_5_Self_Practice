public class C10_Task15_mileConverter {

	public static void main(String[] args) {
		// Write a Java program that converts mile to km
		// 85.0 mile is equal to 136.79424 km
		
		double mile, km;
		
		mile=85.0;
		
		km=mile*1.609344;
		
		System.out.println(mile + " mile is equal to " + km + " km");
		
		
		

	}

}
