package day4_aritmetic_operators;

public class C9_Task14_convertFahrenheitToCelcius {

	public static void main(String[] args) {
		/*Write a Java program to convert Fahrenheit to Celcius
		Sample output:70.2 celcius is equal to 158.36 fahrenheit
		
		fahrenheit = 9 * celcius / 5 + 32
		*/
		
		
		double celcius, fahrenheit;
		
		celcius=70.2;
	
		fahrenheit = 9 * celcius / 5 + 32;
		
		System.out.println(celcius + " celcius is equal to " + fahrenheit + " fahrenheit");
		
		
		
	}

}
