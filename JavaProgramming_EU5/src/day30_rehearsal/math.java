package day30_rehearsal;

public class math {

	public static void main(String[] args) {
		

		
		double result = Math.pow(4.0, 2.0);
		System.out.println(result); //16
		
		double result2 = Math.pow(4.0, 3.0);
		System.out.println(result2); //64
		
		
		
		double res2= Math.sqrt(25.0);
		System.out.println(res2); // 5.0
		
		//final double PI = 3.14;
		
		final double PI = Math.PI;
		System.out.println(PI); //3.141592653589793
		
		
		System.out.println(Math.round(4.3)); //4  to lower
		System.out.println(Math.round(4.5)); //5  to upper
		System.out.println(Math.round(4.6)); //5 to upper
		
		
		System.out.println(Math.floor(4.9)); //4 to bottom limit
		System.out.println(Math.floor(4.3)); //4 to to bottom limit
		
		
		System.out.println(Math.ceil(4.3)); //5 to to bottom limit
		System.out.println(Math.ceil(4.9)); //5 to to bottom limit
		
		
		

	}
	
	
	
	

}
